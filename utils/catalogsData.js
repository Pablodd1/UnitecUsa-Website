export const getCatalogsByLanguage = (language) => {
    return catalogs.filter(cat => cat.language.toLowerCase() === language.toLowerCase());
};

export const getTechnicalSheetsByCategory = (category) => {
    return technicalSheets.filter(sheet => sheet.category.toLowerCase() === category.toLowerCase());
};

export const getGroupedTechnicalSheets = () => {
    const grouped = {};
    technicalSheets.forEach(sheet => {
        if (!grouped[sheet.category]) {
            grouped[sheet.category] = [];
        }
        grouped[sheet.category].push(sheet);
    });
    return grouped;
};
